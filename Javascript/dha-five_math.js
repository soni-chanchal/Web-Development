// Math functions

// 1. Math.sqrt(number): It returns square root of number
console.log(`Square root of 36: ${Math.sqrt(36)}`)

// 2. Math.round(number): It returns rounded to its nearest integer.
console.log(`Rounded value of 7.123: ${Math.round(7.123)}`)

// 3. Math.pow(number, power): It returns the value of x to the power of y. 
console.log(`6 raised to the power 2: ${Math.pow(6,2)}`)

// 4. Math.ceil(number): It returns rounded up to its nearest integer.
console.log(`Ceil value of 5.432: ${Math.ceil(5.432)}`)

// 5. Math.floor(number): It returns rounded down to its nearest integer.
console.log(`Floor value of 6.76: ${Math.floor(6.76)}`)

// 6. Math.trunc(number): It returns the integer part of number.
console.log(`Truncated value 5.678: ${Math.trunc(5.678)}`)

// 7. Math.E: It returns Euler's number
console.log(`Euler's number: ${Math.E}`)

// 8. Math.PI(): It returns value of PI
console.log(`Value of PI: ${Math.PI}`)

// 9. Math.LN2: It returns the natural logarithm of 2.
console.log(`Natural logarithm of 2: ${Math.LN2}`)

// 10. Math.LN10: It returns the natural logarithm of 10.
console.log(`Natural logarithm of 10: ${Math.LN10}`)

// 11. Math.LOG2E: It returns base 2 logarithm of E.
console.log(`Base 2 logarithm of E: ${Math.LOG2E}`)

// 12. Math.LOG10E: It returns base 10 logarithm of E.
console.log(`Base 10 logarithm of E: ${Math.LOG10E}`)

// 13. Math.sqrt: returns the square root of 2
console.log(`Square root of 2: ${Math.SQRT2}`)

// 14. Math.sqrt: returns the square root of 1/2
console.log(`Square root of 1/2: ${Math.SQRT1_2}`)

// 15. Math.abs(number): It returns the absolute (positive) value of number.
console.log(`Absolute (positive) value of -4.7: ${Math.abs(-4.7)}`)

// 16. Math.sin(x): It returns the sine (a value between -1 and 1) of the angle x (given in radians).
console.log(`Value of sin(30): ${Math.sin(30)}`)

// 17. Math.cos(x): It returns the cosine (a value between -1 and 1) of the angle x (given in radians).
console.log(`Value of cos(90): ${Math.cos(90)}`)

// 18.Math.min(): It used to find the lowest value in a list of arguments
console.log(`Min value from the list[0, 150, 30, 20, -8, -200]: ${Math.min(0, 150, 30, 20, -8, -200)}`)

// 19. Math.max(): It used to find the highest value in a list of arguments
console.log(`Max value from the list[0, 150, 30, 20, -8, -200]: ${Math.max(0, 150, 30, 20, -8, -200)}`)

// 20. Math.random(): It returns a random number between 0 (inclusive), and 1 (exclusive):
console.log(`Random value between 0 and 1: ${Math.random()}`)
